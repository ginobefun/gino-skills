#!/usr/bin/env bun
/**
 * Daily Digest Generator
 * 
 * Fetches tech news from multiple sources, filters top 10,
 * generates text + HTML outputs.
 */

import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { join } from 'path';

// Configuration
const OUTPUT_BASE = join(process.env.HOME || '~', '.openclaw/workspace/contents/daily-digest');
const TODAY = new Date().toISOString().split('T')[0];

// Priority 1: Headline companies
const HEADLINE_COMPANIES = [
  'OpenAI', 'Anthropic', 'Google', 'Gemini', 'Meta', 'xAI', 'Microsoft',
  '阿里巴巴', '腾讯', 'DeepSeek', 'Kimi', '智谱', 'GLM'
];

// Priority 2: AI Coding topics
const AI_CODING_KEYWORDS = [
  'Claude Code', 'Codex', 'Cursor', 'Windsurf', 'Copilot',
  'AI coding', 'AI agent', 'MCP protocol'
];

// Priority 3: Trusted sources
const TRUSTED_SOURCES = [
  "Lenny's Podcast", 'Y Combinator', 'The Pragmatic Engineer', 'a16z',
  'Simon Willison', '宝玉', '张小珺', '腾讯科技', '极客公园'
];

interface NewsItem {
  rank: number;
  title: string;
  summary: string;
  source: string;
  url: string;
  score?: number;
  reason: string;
  category: string;
}

/**
 * Filter and rank news items
 */
function filterAndRank(items: any[]): NewsItem[] {
  const scored: Array<NewsItem & { priority: number }> = items.map((item, idx) => {
    let priority = 4; // Default: content quality
    let reason = '内容质量高';

    // Priority 1: Headline companies
    if (HEADLINE_COMPANIES.some(c => item.title.includes(c) || item.source?.includes(c))) {
      priority = 1;
      reason = '头部厂商重大动态';
    }
    // Priority 2: AI Coding
    else if (AI_CODING_KEYWORDS.some(k => item.title.toLowerCase().includes(k.toLowerCase()))) {
      priority = 2;
      reason = 'AI Coding 专题';
    }
    // Priority 3: Trusted sources
    else if (TRUSTED_SOURCES.some(s => item.source?.includes(s))) {
      priority = 3;
      reason = '高优来源';
    }

    return {
      rank: 0,
      title: item.title,
      summary: item.summary || '',
      source: item.source || 'Unknown',
      url: item.url,
      score: item.score,
      reason,
      category: item.category || '综合',
      priority
    };
  });

  // Sort by priority, then by score
  scored.sort((a, b) => {
    if (a.priority !== b.priority) return a.priority - b.priority;
    return (b.score || 0) - (a.score || 0);
  });

  // Take top 10 and assign ranks
  return scored.slice(0, 10).map((item, idx) => ({
    ...item,
    rank: idx + 1,
    priority: undefined // Remove internal field
  } as NewsItem));
}

/**
 * Generate plain text version
 */
function generateText(digest: NewsItem[], date: string): string {
  const lines: string[] = [];
  
  lines.push(`📰 技术早报 | ${date}`);
  lines.push('');

  // Headline (Top 1)
  const top1 = digest[0];
  lines.push('🔥 头条');
  lines.push(`[${top1.rank}] ${top1.title}`);
  lines.push(top1.summary);
  lines.push(`来源：${top1.source} | HN 评分：${top1.score || 'N/A'}`);
  lines.push(top1.url);
  lines.push('');

  // AI Dynamics
  lines.push('⚡ AI 动态');
  digest.slice(1, 5).forEach(item => {
    lines.push(`[${item.rank}] ${item.title}`);
    lines.push(item.summary || '一句话摘要...');
    lines.push(`来源：${item.source} | HN 评分：${item.score || 'N/A'}`);
    lines.push(item.url);
    lines.push('');
  });

  // Deep Reading
  lines.push('💡 深度阅读');
  digest.slice(5, 10).forEach(item => {
    lines.push(`• ${item.title} - ${item.url}`);
  });
  lines.push('');

  // Keywords
  const keywords = extractKeywords(digest);
  lines.push(`🏷️ 关键词：${keywords.join(' / ')}`);
  lines.push('');
  lines.push('---');
  lines.push('Generated by Daily Digest');

  return lines.join('\n');
}

/**
 * Extract keywords from digest
 */
function extractKeywords(digest: NewsItem[]): string[] {
  const allText = digest.map(d => d.title + ' ' + d.summary).join(' ');
  const keywords: string[] = [];
  
  // Simple keyword extraction (can be improved)
  const candidates = ['AI', 'Claude', 'DeepSeek', 'Coding', 'LLM', 'Agent', 'OpenAI', '模型'];
  candidates.forEach(k => {
    if (allText.includes(k)) keywords.push(k);
  });

  return keywords.slice(0, 5);
}

